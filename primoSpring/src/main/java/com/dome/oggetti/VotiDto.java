package com.dome.oggetti;

public class VotiDto {
	private double totaleCampionato;
	private double totaleCampionatoFanta;
	private double campionatoMalus;
	private double totaleCoppa;
	private double totaleCoppaFanta;
	private int malus;

	public double getTotaleCampionato() {
		return totaleCampionato;
	}

	public void setTotaleCampionato(double totaleCampionato) {
		this.totaleCampionato = totaleCampionato;
	}

	public double getTotaleCampionatoFanta() {
		return totaleCampionatoFanta;
	}

	public void setTotaleCampionatoFanta(double totaleCampionatoFanta) {
		this.totaleCampionatoFanta = totaleCampionatoFanta;
	}

	public double getCampionatoMalus() {
		return campionatoMalus;
	}

	public void setCampionatoMalus(double campionatoMalus) {
		this.campionatoMalus = campionatoMalus;
	}

	public double getTotaleCoppa() {
		return totaleCoppa;
	}

	public void setTotaleCoppa(double totaleCoppa) {
		this.totaleCoppa = totaleCoppa;
	}

	public double getTotaleCoppaFanta() {
		return totaleCoppaFanta;
	}

	public void setTotaleCoppaFanta(double totaleCoppaFanta) {
		this.totaleCoppaFanta = totaleCoppaFanta;
	}

	public int getMalus() {
		return malus;
	}

	public void setMalus(int malus) {
		this.malus = malus;
	}

}
